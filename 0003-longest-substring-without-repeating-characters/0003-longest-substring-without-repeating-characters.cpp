class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        int n = s.size();
        vector<int> hash(255, -1);
        int l = 0, r = 0;
        int ans = 0;
        while (r < n) {
            if (hash[s[r]] != -1) {
                if (hash[s[r]] >= l) {
                    l = hash[s[r]] + 1;
                }
            }
            int len = r - l + 1;
            ans = max(ans, len);
            hash[s[r]] = r;
            r++;
        }
        return ans;
    }
};